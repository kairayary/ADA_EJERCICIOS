// Declara dos variables booleanas y realiza una comparación lógica (como AND, 
//     OR). Imprime el resultado.
// Declaración de dos variables booleanas

let booleano1 = true;
let booleano2 = false;

let resultadoAND = booleano1 && booleano2;
console.log("Resultado de AND:", resultadoAND); // false

let resultadoOR = booleano1 || booleano2;
console.log("Resultado de OR:", resultadoOR); // true
